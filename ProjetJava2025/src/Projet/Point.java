package Projet;

public class Point {
	private double x;
	private double y;
	private double z;

	public Point(int x, int y) {
		this.x = x;
		this.y = y;
	}
	public Point(int x, int y, int z) {
		this.x = x;
		this.y = y;
		this.z = z;
	}
	public double getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public double getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	public double getZ() {
		return z;
	}
	public void setZ(int z) {
		this.z = z;
	}

	//qlzjvblqe
}

